def isCorrectRect(rect):
    # проверяет корректность координат прямоугольника
    if len(rect) != 2:
        return False
    # извлекаем координаты из переданных кортежей
    (x1, y1), (x2, y2) = rect
    # проверяем, что координаты левого нижнего угла меньше координат верхнего правого угла
    return x1 < x2 and y1 < y2
# примеры использования
print(isCorrectRect([(-3.4, 1), (9.2, 10)]))  # вернет True
print(isCorrectRect([(-7, 9), (3, 6)]))       # вернет False